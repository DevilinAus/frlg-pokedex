export function slugifyPokemonName(name) {
  return name.toLowerCase().replaceAll("'", '').replaceAll('.', '').replaceAll(' ', '-')
}

function getAvailability(name, version) {
  if (name === 'Mew') {
    return version === 'fireRed' || version === 'leafGreen' ? 'event' : 'native'
  }

  if (fireRedExclusives.has(name)) {
    return version === 'fireRed' ? 'native' : 'trade'
  }

  if (leafGreenExclusives.has(name)) {
    return version === 'leafGreen' ? 'native' : 'trade'
  }

  return 'native'
}

function getEvolutionBaseName(name) {
  let currentName = name

  while (evolvesFrom[currentName]) {
    currentName = evolvesFrom[currentName]
  }

  return currentName === name ? null : currentName
}

